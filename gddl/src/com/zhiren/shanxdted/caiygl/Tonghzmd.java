	public StringBuffer getBaseSql() {
		Visit v = (Visit) this.getPage().getVisit();
		String CurrentDate = DateUtil.FormatOracleDate(getRiq());
		
		StringBuffer sql = new StringBuffer();
			sql.append(
					"select m.mingc, y.mingc, tongh\n" +
					"  from caiyftb c, meikxxb m, yunsdwb y\n" + 
					" where c.meikxxb_id = m.id\n" + 
					"   and c.yunsdwb_id = y.id\n" + 
					"   and c.diancxxb_id="+v.getString1()+"\n" + 
					"   and c.caiyrq="+CurrentDate+"\n" + 
					" order by m.mingc, y.mingc, tongh, caiybh");
		
		return sql;
	}
	
